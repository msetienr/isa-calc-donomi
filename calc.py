class unirCalculator:
    def __init__(self):
        return 0

    def sum(a, b):
        return a + b

    def multiply(a, b):
        return a * b

    def divide(a, b):
        if b != 0:
            return a / b

    def square_root(a):
        if a >= 0:
            return a ** 0.5

    def exp_e(x):
        if x != 0:
            return 2.71828 ** x